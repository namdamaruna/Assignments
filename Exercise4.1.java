// Write a program to perform bitwise AND and OR operation on x=10 and y=20

class Exercise4 {
    public static void main(String []args){
        int x=10;
        int y=20;
        
        System.out.println("x&y="+ (x & y));
        
        System.out.println("x|y="+ (x | y));
            
    }



}