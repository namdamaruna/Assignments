// Write a program to perform bitwise Left shift(<<) and right shift(>>) operation on x=10 and y=20

class Exercise5 {
    public static void main(String[]args){
        int x=10;
        int y=20;
        System.out.println("x>>y="+(x >> y));
        System.out.println("x<<y="+(x <<y));
        
    }




}