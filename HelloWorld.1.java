public class HelloWorld {
	public static void main(String args[]) {
	
		int x=10, y=20;
		
		System.out.println(++x);
		System.out.println(--y);
		System.out.println(x++ + ++y);
		System.out.println(x-- + y++);
    	boolean e=true;
    	boolean d=false;
    	System.out.println(!e);
    	System.out.println(!d);
	}
}